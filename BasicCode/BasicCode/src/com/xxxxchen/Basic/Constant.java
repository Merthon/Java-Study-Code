package com.xxxxchen.Basic;

/*
常量：在程序执行过程中，其值不会改变的量
*/
public class Constant {
    public static void main(String[] args) {
        System.out.println("字符串常量");  //字符串常量
        System.out.println(123);   //整数常量
        System.out.println(12.5);   //小数常量
        System.out.println('a');    //字符常量，只能表达单个字符
        System.out.println(false);   //布尔常量
        System.out.println(true);   //布尔常量
        System.out.println(1 > 2);   //布尔常量
//        System.out.println(null);   //null不能作为输出语句打印
    }
}
