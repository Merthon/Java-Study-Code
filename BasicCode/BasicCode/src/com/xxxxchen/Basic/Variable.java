package com.xxxxchen.Basic;

public class Variable {
    public static void main(String[] args) {
        //        格式： 数据类型  变量名 = 数据值
        //整数
        int a = 10;
        System.out.println(a);
        //浮点数
        double b = 17.9;
        System.out.println(b);
        //布尔
        boolean c = false;
        System.out.println(c);
        //字符
        char d = 'l';
        System.out.println(d);
    }
}
