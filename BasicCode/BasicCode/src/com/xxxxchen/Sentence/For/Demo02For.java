package com.xxxxchen.Sentence.For;

/*案例：
 利用for循环控制台打印输出1-5和 5-1
 */
public class Demo02For {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        for(int i = 5; i >= 1; i--){
            System.out.println(i);
        }
    }

}
